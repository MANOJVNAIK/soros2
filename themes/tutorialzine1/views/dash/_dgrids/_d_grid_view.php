                            <script type="text/javascript">
                             
                             
                             var SM_callback = {
                                              
                                              SM_execute : function(SM_stored_values){
                                                                     var fetchDashboardElements = 'fetchDashboardElements';
                                                                     var gadgetType             = 'System_Messages';
                                                                     
                                                                           
                                                                                                                                           
                                                                     $.ajax({
                                                                      type: 'POST',
                                                                      url: "/Helios_Jan_4/dash/Dash",     //UIDashboardController::Dash().. 
                                                                      data: {'ajaxForwardRequest' : fetchDashboardElements, 'gadgetType' : gadgetType},
                                                                      success: function(stringified){
                                                                       
                                                                        var data_obj = $.parseJSON(stringified);
                                                                        //console.log(data_obj);
                                                                         var columns_index = data_obj.length - 1;
                                                                         
                                                                         
                                                                         //@render thead..
                                                                         var thead_children = "";
                                                                         for(var hb_i=0; hb_i < data_obj[columns_index].length; ++hb_i ){
                                                                          thead_children += "<th class='ui-state-default' style='width: 107px;' ><div class='DataTables_sort_wrapper'>" + data_obj[columns_index][hb_i] + "<span class='css_right ui-icon ui-icon-triangle-1-n'></span></div></th>";
                                                                         }
                                                                         
                                                                        
                                                                        // @tbody begin & end                
                                                                        var tbody_begin = [
                                                                                           "<tr class='gradeX odd'>"
                                                                                          ].join('\n');   
                                                                         
                                                                                          
                                                                        var tbody_end = [
                                                                                         "</tr>"
                                                                                        ].join('\n');
                                                                                        
                                                                          
                                                                         //@render tbody..
                                                                         var tbody_children = "";
                                                                         for(var hb_j=0; hb_j < data_obj.length - 1; ++hb_j){
                                                                            tbody_children += tbody_begin;
                                                                          for(var hb_k=1; hb_k < data_obj[hb_j].length - 1; ++hb_k ){
                                                                           if(hb_k == 1)
                                                                           {
                                                                            tbody_children += "<td class='sorting_1'>" + data_obj[hb_j][hb_k] + "</td>";
                                                                           }
                                                                           else{
                                                                            tbody_children += "<td>" + data_obj[hb_j][hb_k] + "</td>";
                                                                           }
                                                                          }
                                                                            tbody_children += tbody_end;
                                                                         }
                                                                        
                                                                        
                                                                        var parent_table = document.getElementById('example');
                                                                         parent_table.innerHTML = "";
                                                                          
                                                                        var thead_parent = document.createElement('thead');
                                                                        var tbody_parent = document.createElement('tbody');
                                                                        
                                                                        // @thead begin & end
                                                                        var thead_begin = [ 
                                                                                           "<thead>",
                                                                                             "<tr>"
                                                                                          ].join('\n');
                                                                        
                                                                        var thead_end = [
                                                                                         "</tr>",
                                                                                          "</thead>"
                                                                                        ].join('\n');
                                                                                        
                                                                                        
                                                                        thead_parent.innerHTML = thead_begin + thead_children + thead_end;
                                                                        tbody_parent.innerHTML = tbody_children;
                                                                        
                                                                        parent_table.appendChild(thead_parent);
                                                                        parent_table.appendChild(tbody_parent);
                                                                              
                                                                       
                                                                      }     //success..
                                                                     });  //ajax..
                                                                     
                                                                     return true;
                                                                     
                                              }
                                              
                             }
                             
                            
                              SM_callback.SM_execute(); 
                              
                              
                            </script>
                            
                            
                            